    private void btnUpdate2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdate2ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a category.", "Warnning", 2);
        } else {
            int id = Integer.parseInt(jTextField1.getText());
            if (cat.isIDExist(id)) {
                cat.delete(id);
                jTable1.setModel(new DefaultTableModel(null, new Object[]{"Catagory ID", "Catagory Name", "Description"}));
                cat.getCategoriesValue(jTable1, "");
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Category Doesn't exist", "Warnning", 2);
            }
        }
    }//GEN-LAST:event_btnUpdate2ActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        //save
        if (isEmpty()) {
            int id = Integer.parseInt(jTextField1.getText());
            String cname = jTextField2.getText();
            String desc = jTextField3.getText();
            if (!cat.isIDExist(id)) {
                if (!cat.isCategoryNameExist(cname)) {
                    cat.insert(id, cname, desc);
                    jTable1.setModel(new DefaultTableModel(null, new Object[]{"Catagory ID", "Catagory Name", "Description"}));
                    cat.getCategoriesValue(jTable1, "");
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Category name already exists", "Warnning", 2);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Category id already exists", "Warnning", 2);
            }

        }
    }//GEN-LAST:event_btnUpdateActionPerformed

