    private void init() {
        jTextField1.setBackground(notEdit);
        jTextField1.setText(String.valueOf(product.getMaxRow()));
        categories = new String[product.countCategories()];
        setCat();
        productTable();
//        setLocation(450,180);
    }

    private void setCat() {
        categories = product.getCat();
        for (String s : categories) {
            jComboBox1.addItem(s);
        }
    }

    private void clear() {
        jTextField4.setText("");
        jTextField1.setText(String.valueOf(product.getMaxRow()));
        jTextField5.setText("");
        jComboBox1.setSelectedIndex(0);
        jTextField2.setText("0");
        jTextField3.setText("0.0");
        jTable1.clearSelection();
        statistics.admin();
    }

    private void productTable() {

        product.getProductsValue(jTable1, "");
        model = (DefaultTableModel) jTable1.getModel();
        jTable1.setRowHeight(30);
        jTable1.setShowGrid(true);
        jTable1.setGridColor(Color.BLACK);
        jTable1.setBackground(Color.white);
        jTable1.setSelectionBackground(Color.LIGHT_GRAY);

    }

    private boolean isEmpty() {
        if (jTextField5.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Product name is required", "Warnning", 2);
            return false;
        }
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "product price is required.", "Warnning", 2);
            return false;
        }
        if (Integer.parseInt(jTextField2.getText()) <= 0) {
            JOptionPane.showMessageDialog(this, "Please, increase the product quantity", "Warnning", 2);
            return false;
        }
        if (jTextField3.getText().equals("0.0") || jTextField3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Product price is required", "Warnning", 2);
            return false;
        }
        return true;
    }

