    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (isEmpty()) {
            int id = Integer.parseInt(jTextField1.getText());
            String pname = jTextField5.getText();
            String cat = jComboBox1.getSelectedItem().toString();
            int qty = Integer.parseInt(jTextField2.getText());
            if (isNumeric(jTextField3.getText())) {
                double price = Double.parseDouble(jTextField3.getText());
                if (!product.isIDExist(id)) {
                    if (!product.isProcatExist(pname, cat)) {
                        product.insert(id, pname, cat, qty, price);
                        jTable1.setModel(new DefaultTableModel(null, new Object[]{"Product ID", "Product Name", "Catagory name", "Quabtity", "Price"}));
                        product.getProductsValue(jTable1, "");
                        clear();
                    } else {
                        JOptionPane.showMessageDialog(this, "Product and category already exist", "Warnning", 2);
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Product id already exists", "Warnning", 2);
                }
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

