    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //save button
        if (isEmpty()) {
            int id = Integer.parseInt(jTextField4.getText());
            String username = jTextField2.getText();
            String email = jTextField1.getText();
            String password = String.valueOf(jPasswordField1.getPassword());
            String phone = jTextField7.getText();
            String seq = jComboBox1.getSelectedItem().toString();
            String ans = jTextField3.getText();
            String address1 = jTextField5.getText();
            String address2 = jTextField6.getText();
            if (!user.isEmailExist(email)) {
                if (!user.isPhoneExist(phone)) {
                    user.insert(id, username, email, password, phone, seq, ans, address1, address2);
                    new Login().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "This phone number already exists", "Warnning", 2);
                }
            } else {
                JOptionPane.showMessageDialog(this, "This email address already exists", "Warnning", 2);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

